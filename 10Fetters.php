<p>The Pali canon's Sutta Pitaka identifies ten "fetters of becoming":</p>
<ol>
	<li>belief in a self (<em><em lang="sa-Latn" title="International Alphabet of Sanskrit transliteration">sakkāya-diṭṭhi</em></em>)</li>
	<li>doubt or uncertainty, especially about the Buddha's awakeness and nine supermundane consciousnesses (<em>vicikicchā</em>)</li>
	<li>attachment to rites and rituals (<em>sīlabbata-parāmāsa</em>)</li>
	<li>sensual desire (<em>kāmacchando</em>)<sup id="cite_ref-10" class="reference">[</sup></li>
	<li>ill will (<em>vyāpādo</em>&nbsp;or&nbsp;<em>byāpādo</em>)</li>
	<li>lust for material existence, lust for material rebirth (<em>rūparāgo</em>)</li>
	<li>lust for immaterial existence, lust for rebirth in a formless realm (<em>arūparāgo</em>)</li>
	<li>conceit (<em>māna</em>)</li>
	<li>restlessness (<em>uddhacca</em>)</li>

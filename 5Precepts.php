<h3><span id="Moral_discipline_and_precepts_(Sīla_&bull;_Śīla)" class="mw-headline">Precepts (<em>Sīla</em>)</span></h3>
<p>Five Precepts&nbsp;(<em>pa&ntilde;ca-sīlāni</em>&nbsp;&bull;&nbsp;<em>pa&ntilde;ca-śīlāni</em>)</p>
<ol>
<li>Abstaining&nbsp;from taking life (<em>pāṇātipātā veramaṇī</em>)</li>
<li>Abstaining from&nbsp;taking what is not given&nbsp;(<em>adinnādānā veramaṇī</em>)</li>
<li>Abstaining from&nbsp;sexual misconduct&nbsp;(<em>kāmesu micchācāra veramaṇī</em>)</li>
<li>Abstaining from&nbsp;false speech&nbsp;(<em>musāvāda veramaṇī</em>)</li>
<li>Abstaining from&nbsp;drinks&nbsp;and&nbsp;drugs&nbsp;that cause heedlessness (<em>surā-meraya-majja-pamādaṭṭhānā veramaṇī</em>)</li>
</ol>
<p>Eight Precepts&nbsp;(<em>aṭṭhasīla</em>)</p>
<ol>
<li>Abstaining from taking life (both&nbsp;human&nbsp;and&nbsp;non-human)</li>
<li>Abstaining from taking what is not given (stealing)</li>
<li>Abstaining from all&nbsp;sexual activity</li>
<li>Abstaining from telling&nbsp;lies</li>
<li>Abstaining from using intoxicating&nbsp;drinks&nbsp;and&nbsp;drugs&nbsp;which lead to carelessness</li>
<li>Abstaining from&nbsp;eating&nbsp;at the wrong time (the right time is eating once, after&nbsp;sunrise, before&nbsp;noon)</li>
<li>Abstaining from&nbsp;singing,&nbsp;dancing, playing music, attending&nbsp;entertainment&nbsp;performances, wearing&nbsp;perfume, and using&nbsp;cosmetics&nbsp;and&nbsp;garlands&nbsp;(decorative accessories)</li>
<li>Abstaining from&nbsp;luxurious&nbsp;places for sitting or sleeping</li>
</ol>
<p>Ten Precepts&nbsp;(<em>dasasīla</em>)</p>
<ol>
<li>Abstaining from killing living things</li>
<li>Abstaining from stealing</li>
<li>Abstaining from un-chastity (sensuality, sexuality, lust)</li>
<li>Abstaining from lying</li>
<li>Abstaining from taking intoxicants</li>
<li>Abstaining from taking food at inappropriate times (after noon)</li>
<li>Abstaining from singing, dancing, playing music or attending entertainment programs (performances)</li>
<li>Abstaining from wearing perfume, cosmetics and garland (decorative accessories)</li>
<li>Abstaining from sitting on high chairs and sleeping on luxurious, soft beds</li>
<li>Abstaining from accepting&nbsp;money</li>
</ol>
